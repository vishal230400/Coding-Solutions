package com.vishal.codechef.ANDOR;

import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while(t-->0)
        {
            long x=sc.nextLong();
            System.out.println(0+" "+x);
        }
        sc.close();
    }
}